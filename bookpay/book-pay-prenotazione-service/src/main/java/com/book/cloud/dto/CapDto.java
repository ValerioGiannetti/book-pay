package com.book.cloud.dto;

public class CapDto implements java.io.Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5815091940380609503L;
	private int istat;
	private String cap;

	public CapDto() {
	}

	public int getIstat() {
		return istat;
	}

	public void setIstat(int istat) {
		this.istat = istat;
	}

	public String getCap() {
		return cap;
	}

	public void setCap(String cap) {
		this.cap = cap;
	}

	

}
