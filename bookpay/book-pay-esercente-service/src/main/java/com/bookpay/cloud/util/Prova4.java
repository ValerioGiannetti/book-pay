package com.bookpay.cloud.util;

public class Prova4 {

	private String idS;
	private String nomeS;
	
	public Prova4(){}
	
	public Prova4(String idS,String nomeS) {
		
		this.idS = idS;
		this.nomeS = nomeS;
	}
	
	
	public String getIdS() {
		return idS;
	}
	public void setIdS(String idS) {
		this.idS = idS;
	}
	public String getNomeS() {
		return nomeS;
	}
	public void setNomeS(String nomeS) {
		this.nomeS = nomeS;
	}
	
	
}
